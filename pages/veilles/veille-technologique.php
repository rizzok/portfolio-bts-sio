                        </div>
                    </li>
                    <li><b>Dépendance à internet</b> : obligation d’être connecté à internet pour accéder aux données.</li>
                    <li><b>Le coût</b> : selon les besoins et l'utilisation du cloud, le coût peut être beaucoup plus élevé qu'en infrastructure locale. Surtout si la taille du stockage est conséquente, les frais peuvent rapidement augmenter.</li>
                    <li><b>Cadre légal</b> : les données peuvent être stockées à l'étranger, l'entreprise doit alors s'adapter aux lois en vigueur.</li>
                </ul>
