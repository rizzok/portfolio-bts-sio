                <h2 class="mb-3">Sujet principal de la veille : le cloud computing</h2>
            </div>
            <div class="my-4">
                <h3 class="h4">Qu'est-ce que le cloud computing ?</h3>
                <p class="text-justify m-3">
                    Le cloud computing consiste à externaliser des infrastructures informatiques dans des serveurs distants, accessibles par l’intermédiaire d’un réseau, généralement internet. Le but est de pouvoir exploiter la puissance de calcul et le stockage d’un serveur distant, à partir d’un navigateur internet ou d’une application cliente légère.<br>
                    C'est une évolution des technologies de virtualisation, qui constitue une réponse à la réalité économique et aux pressions croissantes visant une réduction des coûts et une augmentation de l'efficacité et de l'agilité des insfrastructures informatiques.
                </p>
            </div>
            <div class="my-4">
                <h3 class="h4 mb-3">Les principaux avantages</h3>
                <ul>
                    <li><b>Service à la demande</b> : la solution du cloud permet la prise en charge des besoins de l’organisation en garantissant un fonctionnement stable même lors des pics d’utilisation ou lors de l’évolution des besoins. On ne raisonne plus en nombre de serveurs, en taille de disques physique, en nombre de processeurs... mais en puissance de calcul, en capacité totale de stockage et en bande passante disponible.</li>
                    <li><b>Agilité</b> : Il est possible de disposer de plus de ressources très rapidement pour soutenir une forte demande comme lors d’un pic d’activité d’une entreprise commerciale durant les fêtes de fin d’année). Inversement, il est possible avec le Cloud de diminuer les ressources utilisées l’activité diminue. L’entreprise gagne en agilité, en adaptant plus facilement.</li>
                    <li><b>Accès universel</b> : différents appareils (ordinateurs, smartphones, tablettes, etc.) peuvent accéder au cloud quelle que soit leur localisation géographique.</li>
                    <li><b>Service mesurable et facturable</b> : le fournisseur de la solution de Cloud peut mesurer précisément la consommation des différentes ressources (CPU, Stockage, bande passante...) et facturer au plus juste cet usage à l’organisation cliente.</li>
                </ul>
            </div>
            <div class="my-4">
                <h3 class="h4 mb-3">Les principaux inconvénients</h3>
                <ul>
                    <li><b>La sécurité</b> :
                        Si le service Cloud est piraté, les données sensibles de l'entreprise ou de des clients peuvent finir exposées.
                        Il existe aussi un risque de fuite de données en cas d’erreur de configuration du service. Beaucoup d’entreprises en ont été victimes, même parmi les plus grandes.
                        La principale faille d'un système informatique étant ses utilisateurs, il est très difficile, de maîtriser et de s'assurer que toutes les actions soient faites de manière sécurisées.
