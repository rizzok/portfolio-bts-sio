            <div class="my-4">
                <h3 class="h4 mb-3">Les différents types de services du cloud</h3>
                <ul>
                    <li class="h5"><b>Iaas</b> (<b>I</b>nfrastructure <b>a</b>s <b>a</b> <b>S</b>ervice)</li>
                    <p class="text-justify">Les calculs, le stockage, la mise en réseau et d’autres éléments (comme la sécurité ou les outils) sont assurés par le fournisseur IaaS. Les utilisateurs sont propriétaires des systèmes d’exploitation, des applications et des informations présents sur l’infrastructure, et ils en assurent la gestion.</p>
                    <li class="h5"><b>PaaS</b> (<b>P</b>latform <b>a</b>s <b>a</b> <b>S</b>ervice)</li>
                    <p class="text-justify">Tous les logiciels et le matériel requis pour créer et exécuter des applications basées sur le Cloud sont fournis par le fournisseur PaaS. Les utilisateurs paient l’utilisation de la plateforme et contrôlent les modalités d’utilisation des applications tout au long de leur cycle de vie.</p>
                    <li class="h5"><b>SaaS</b> (<b>S</b>oftware <b>a</b>s <b>a</b> <b>S</b>ervice)</li>
                    <p class="text-justify">Le logiciel s’exécute sur des ordinateurs qui appartiennent au fournisseur SaaS et sont gérés par lui, au lieu d’être installé et géré sur les ordinateurs des utilisateurs. Accessible via l’Internet public, le logiciel est habituellement proposé par voie d’abonnement mensuel ou annuel.</p>
                    <p class="text-justify">Exemples de SaaS populaires : Gmail, Google Map, Google Drive, Google Docs, Microsoft 365, Salesforce (CRM), iCloud, Dropbox</p>
                </ul>
