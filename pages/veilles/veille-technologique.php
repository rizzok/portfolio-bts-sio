                <h3 class="h4 mb-3">Les 4 modèles de déploiement</h3>
                <ol>
                    <li class="h5">Le Cloud Public :</li>
                    <p class="text-justify">Le fournisseur de la solution de Cloud est externe à l’organisation et son infrastructure ainsi que ses services sont accessibles à tout le monde. Quand le client ne fait que « consommer » du logiciel sous la forme d’un service, on parle de modèle SaaS (Software as a Service).</p>
                    <li class="h5">Le Cloud Privé :</li>
                    <p class="text-justify">Le cloud est interne à l’organisation qui en est le propriétaire. C’est l’évolution des datacenters virtualisés. Les équipes informatiques qui gèrent le cloud comme un centre de service dont les prestation sont facturées aux différentes division de l’organisation.</p>
                    <li class="h5">Le Cloud Communautaire :</li>
                    <p class="text-justify">Les ressources, services et la propriété sont partagées à l’échelle d’une communauté (à l’échelle d’une académie, d’un GIE...).</p>
                    <li class="h5">Le Cloud Hybride :</li>
                    <p class="text-justify">C’est la combinaison du Cloud privé et public. Par exemple une organisation peut gérer un cloud privé pour des services sensibles comme des applications métiers et utiliser un cloud public pour des besoins plus standards pour les besoins bureautiques. Ce modèle est amené à se développer dans les années à venir.</p>
                </ol>
