                    <p class="text-justify">La sécurité restant le gros inconvénient du cloud, le modèle du Cloud Hybride est surement le meilleur compromis pour les entreprises. Ce modèle combine les avantages du Cloud privé à ceux du Cloud public, et permet de transférer les données de l’un à l’autre très facilement. Par exemple, une entreprise pourra déployer un Cloud privé sur site afin d’héberger des flux de donées sensibles ou critiques, mais pourra faire appel à un fournisseur de Cloud public tiers, pour héberger des ressources moins critiques, par exemple destinées aux tests et au développement.</p>
